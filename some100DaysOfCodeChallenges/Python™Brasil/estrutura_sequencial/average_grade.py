# Create a program that asks for the 4 quarterly grades and displays the average.
# Let's gooooooooooo!
grade1 = input("Enter the first grade: ")
grade2 = input("Enter the second grade: ")
grade3 = input("Enter the third grade: ")
grade4 = input("Enter the fourth grade: ")

avg = (int(grade1) + int(grade2) + int(grade3) + int(grade4)) / 4

print(f"The avarage for the quarterly grades is: {avg}")
