'''
 There are 2,204 pounds in a kilogram. 
 Ask the user to enter a weight in kilograms and convert it 
 to pounds.
'''
# Read input from the user
weight_in_kilograms = float(input("Enter weight in kilograms: "))

# Conversion
weight_in_pounds = weight_in_kilograms * 2.204

# Display the result
print(f"{weight_in_kilograms} kilograms is equal to {weight_in_pounds} pounds.")
